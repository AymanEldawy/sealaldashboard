import { InputField } from "@/components/Forms/InputField";
import { Button } from "@/components/Global/Button/Button";
import EyeSlashIcon from "@/components/Icons/EyeSlashIcon";
import { SignInWith } from "@/components/SignInWith/SignInWith";
import SignupCompanyForm from "@/components/SignupCompanyForm/SignupCompanyForm";
import SignupCustomerForm from "@/components/SignupCustomerForm/SignupCustomerForm";
import SignupServiceForm from "@/components/SignupServiceForm/SignupServiceForm";
import SignupTabBar from "@/components/SignupTabBar/SignupTabBar";
import { fetchWord } from "@/lang/fetchWord";
import Head from "next/head";
import Image from "next/image";
import Link from "next/link";
import { useContext, useState } from "react";

import { LanguageContext } from "../context/LangContext";

export default function NewPassword() {
  const { lang } = useContext(LanguageContext);
  const [passwordType, setPasswordType] = useState("password");
  const [password, setPassword] = useState("");
  const [confirmPassword, setConfirmPassword] = useState('');
  const [stage, setStage] = useState(1);


  const showPassword = () => {
    setPasswordType(prev => prev === 'password' ? 'text' : 'password')
  }


  const submit = (e) => {
    e.preventDefault();
    setStage(2)
  };
  return (
    <>
      <Head>
        <title>{fetchWord("new_password", lang)}</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="container flex items-center min-h-screen relative" >

        <Image
          className="object-contain absolute top-4 ltr:left-6 rtl:right-6"
          src="/images/logo.png"
          alt="register"
          height={70}
          width={130}
        />
        <div className="flex-1 flex justify-center">
          <Image
            className="object-contain"
            src={stage === 1 ? "/images/authbanner.png" : '/images/success.png'}
            alt="register"
            height={480}
            width={420}
          />
        </div>
        <div className="flex flex-col flex-1 items-center mb-8 overflow-auto">
          <div className="flex flex-col flex-1 mt-24 items-center w-full max-w-[500px]">
            <div className="flex flex-col w-full max-w-[500px]">
              {
                stage === 1 ? (
                  <>
                    <h1 className="text-2xl mb-2 text-center text-secondary capitalize font-semibold">
                      {fetchWord("new_password_title", lang)}
                    </h1>
                    <p className="text-center text-ptext mb-6">{fetchWord('signin_msg', lang)}</p>
                    <form onSubmit={submit}>
                      <InputField
                        type={passwordType}
                        name="password"
                        value={password}
                        label={fetchWord("new_password", lang)}
                        placeholder={fetchWord("enter_password", lang)}
                        onChange={(e) => setPassword(e.target.value)}
                        iconEnd={<button type="button" onClick={showPassword} className="mt-1 ltr:mr-1 rtl:ml-1">
                          <EyeSlashIcon className={passwordType === 'text' ? 'text-primary' : ''} />
                        </button>}
                      />
                      <InputField
                        type={passwordType}
                        name="confirmPassword"
                        value={confirmPassword}
                        label={fetchWord("repeat_password_label", lang)}
                        placeholder={fetchWord("repeat_password_msg", lang)}
                        onChange={(e) => setConfirmPassword(e.target.value)}
                        iconEnd={<button type="button" onClick={showPassword} className="mt-1 ltr:mr-1 rtl:ml-1">
                          <EyeSlashIcon className={passwordType === 'text' ? 'text-primary' : ''} />
                        </button>}
                      />
                      <Button classes="!p-3 block w-full">{fetchWord("submit", lang)}</Button>
                    </form>
                  </>
                ) : (
                  <div className="flex flex-col gap-1 items-center">
                    <h2 className="text-2xl font-semibold text-green-500">{fetchWord('password_updated', lang)}</h2>
                    <p className="">{fetchWord('password_updated_msg', lang)}</p>
                    <Link href="/login" className="text-white text-center w-full mt-4 bg-primary rounded-lg p-3 block capitalize">{fetchWord('login', lang)}</Link>
                  </div>
                )}

            </div>
          </div>
        </div>
      </div>

    </>
  );
}
